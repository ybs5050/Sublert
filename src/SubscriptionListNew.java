    @Override
    public boolean checkNull() {
        return name.getText().equals("Name") || price.getText().equals("Price") || date.getText().equals("Date (mm-dd-YYYY)")
                || name.getText().isEmpty() || price.getText().isEmpty() || date.getText().isEmpty();
    }
