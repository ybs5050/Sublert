    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        boolean checkNull = checkNull();
        if(!checkNull) {
            try {
            double subPrice = Double.parseDouble(price.getText());
            subscriptionCtrl.addSubscription(name.getText(), subPrice, date.getText());
            SubscriptionListNew.this.setAlwaysOnTop(true);
            this.subscriptionCtrl.subList.writeSubscriptionListFile();
            this.subscriptionCtrl.subList.printSubscriptionList();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Please Enter Numeric Value", "Error", HEIGHT);
            }
        }
    }//GEN-LAST:event_addMouseClicked


